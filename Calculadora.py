class Calculadora:
    def Soma(self, numero1,numero2):
        return numero1 + numero2


    def Subtracao(self, numero1, numero2):
        return numero1 - numero2


    def Divisao(self, numero1, numero2):
        return numero1 / numero2


    def Multiplicacao(self, numero1,numero2):
        return numero1 * numero2    
    

